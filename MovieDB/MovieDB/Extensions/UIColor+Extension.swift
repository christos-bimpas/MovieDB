import UIKit.UIColor

extension UIColor {
    
    class var whiteSmoke: UIColor {
        return UIColor(red: 235/255.0, green: 235/255.0, blue: 235/255.0, alpha: 1.0)
    }
    
    class var darkGreen: UIColor {
        return UIColor(red: 4/255.0, green: 25/255.0, blue: 34/255.0, alpha: 1.0)
    }
    
    class var charcoal: UIColor {
        return UIColor(red: 70/255.0, green: 70/255.0, blue: 70/255.0, alpha: 1.0)
    }
}
